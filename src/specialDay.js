/*
    특별한 날

    2월 19일은 hoony에게 특별한 날입니다.
    specialDay 함수는 매개변수로 월과 일자인 month와 day를 받습니다. 
    해당 월과 일자가 2월 19일보다 이전이면 'Before', 이후라면 'After',
    당일이라면 'Special'을 리턴해주세요. 
		
    - month는 1에서 12사이, day는 1에서 31사이의 정수입니다.

    입출력 예시
    ------------------------------
    input
    ------------------------------

    specialDay(2, 15)

    ------------------------------
    output
    ------------------------------

    'Before'
*/

function specialDay(month, day) {
  let answer = "";

  if (month > 2) answer = "After";
  else if (month < 2) answer = "Before";
  else {
    // month === 2
    if (day > 19) answer = "After";
    else if (day < 19) answer = "Before";
    else answer = "Special"; // day === 19
  }

  return answer;
}

module.exports = specialDay;
